def get_today_greeting() -> str:
    """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç ¬´—Å–≤–æ—ë¬ª –ø–æ–∂–µ–ª–∞–Ω–∏–µ –Ω–∞ —ç—Ç–æ—Ç –¥–µ–Ω—å –ø–æ –ö–∏–µ–≤—É."""
    now_kyiv = datetime.now(KYIV_TZ).date()
    idx = now_kyiv.toordinal() % len(GREETINGS)
    return GREETINGS[idx]


async def send_daily_greeting(context: ContextTypes.DEFAULT_TYPE) -> None:
    """–û—Ç–ø—Ä–∞–≤–∫–∞ –µ–∂–µ–¥–Ω–µ–≤–Ω–æ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è –ø–æ —Ä–∞—Å–ø–∏—Å–∞–Ω–∏—é."""
    job = context.job
    text = get_today_greeting()
    await context.bot.send_message(chat_id=job.chat_id, text=text)


async def start(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """–ö–æ–º–∞–Ω–¥–∞ /start: —Å—Ç–∞–≤–∏–º –µ–∂–µ–¥–Ω–µ–≤–Ω—É—é —Ä–∞—Å—Å—ã–ª–∫—É –≤ 9:00 –ø–æ –ö–∏–µ–≤—É."""
    chat_id = update.effective_chat.id

    # –£–¥–∞–ª—è–µ–º —Å—Ç–∞—Ä—É—é –∑–∞–¥–∞—á—É (–µ—Å–ª–∏ –±—ã–ª–∞), —á—Ç–æ–±—ã –Ω–µ –¥—É–±–ª–∏—Ä–æ–≤–∞—Ç—å
    for old_job in context.job_queue.get_jobs_by_name(str(chat_id)):
        old_job.schedule_removal()

    # –°–æ–∑–¥–∞—ë–º –µ–∂–µ–¥–Ω–µ–≤–Ω—É—é –∑–∞–¥–∞—á—É
    context.job_queue.run_daily(
        send_daily_greeting,
        time=time(hour=9, minute=0, tzinfo=KYIV_TZ),
        days=(0, 1, 2, 3, 4, 5, 6),  # –∫–∞–∂–¥—ã–π –¥–µ–Ω—å –Ω–µ–¥–µ–ª–∏
        name=str(chat_id),
        chat_id=chat_id,
    )

    await update.message.reply_text(
        "–ì–æ—Ç–æ–≤–æ üåû\n"
        "–Ø –±—É–¥—É –ø–∏—Å–∞—Ç—å —Ç–µ–±–µ –¥–æ–±—Ä–æ–µ —É—Ç—Ä–æ –∫–∞–∂–¥—ã–π –¥–µ–Ω—å –≤ 09:00 –ø–æ –ö–∏–µ–≤—É.\n"
        "–ï—Å–ª–∏ –∑–∞—Ö–æ—á–µ—à—å –æ—Å—Ç–∞–Ω–æ–≤–∏—Ç—å —Ä–∞—Å—Å—ã–ª–∫—É ‚Äî –Ω–∞–ø–∏—à–∏ /stop."
    )


async def stop(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """–ö–æ–º–∞–Ω–¥–∞ /stop: –≤—ã–∫–ª—é—á–∞–µ–º –µ–∂–µ–¥–Ω–µ–≤–Ω—ã–µ –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏—è –¥–ª—è —ç—Ç–æ–≥–æ —á–∞—Ç–∞."""
    chat_id = update.effective_chat.id
    jobs = context.job_queue.get_jobs_by_name(str(chat_id))

    if not jobs:
        await update.message.reply_text("–£ —Ç–µ–±—è –∏ —Ç–∞–∫ –Ω–µ—Ç –∞–∫—Ç–∏–≤–Ω–æ–π —É—Ç—Ä–µ–Ω–Ω–µ–π —Ä–∞—Å—Å—ã–ª–∫–∏ üôÇ")
        return

    for job in jobs:
        job.schedule_removal()

    await update.message.reply_text("–£—Ç—Ä–µ–Ω–Ω—è—è —Ä–∞—Å—Å—ã–ª–∫–∞ –æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∞. –ï—Å–ª–∏ –ø–µ—Ä–µ–¥—É–º–∞–µ—à—å ‚Äî /start.")


def main() -> None:
    application = (
        ApplicationBuilder()
        .token(BOT_TOKEN)
        .build()
    )

    application.add_handler(CommandHandler("start", start))
    application.add_handler(CommandHandler("stop", stop))

    # Render –¥–∞—ë—Ç –ø–æ—Ä—Ç —á–µ—Ä–µ–∑ –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é PORT
    port = int(os.environ.get("PORT", "8080"))

    application.run_webhook(
        listen="0.0.0.0",
        port=port,
        url_path=BOT_TOKEN,
        webhook_url=f"{APP_URL}/{BOT_TOKEN}",
    )


if __name__ == "__main__":
    main()
